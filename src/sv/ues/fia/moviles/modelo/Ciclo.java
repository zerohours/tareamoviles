package sv.ues.fia.moviles.modelo;

public class Ciclo {
	
	private int idCiclo;
	private int anio;
	private int numero;

	public int getId() {
		return idCiclo;
	}

	public void setId(int idCiclo) {
		this.idCiclo = idCiclo;
	}

	public int getAnio() {
		return anio;
	}

	public void setAnio(int anio) {
		this.anio = anio;
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}
	

}
